algo = input("Digite Algo: ")

print("É um Alphanumérico? --> {}".format(algo.isalnum()))
print("É um Aphabético? --> {}".format(algo.isalpha()))
print("É um numero Decimal? --> {}".format(algo.isdecimal()))
print("Está em letras Minúsculas? --> {} ".format(algo.islower()))
print("É em letras Maiúsculas? --> {} ".format(algo.isupper()))
